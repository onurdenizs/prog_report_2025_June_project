# Main execution
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
def main():
    logging.info(f"ğŸ“¥ Loading raw simplified line geometry from: {INPUT_CSV}")

    try:
        df = pd.read_csv(INPUT_CSV, sep=DELIMITER)
    except Exception as e:
        logging.error(f"âŒ Failed to load input file: {e}")
        sys.exit(1)

    # Validate column presence
    required_cols = {'START_OP', 'END_OP', 'START_OP.1', 'END_OP.1', 'Linie', 'Line', 'Geo shape'}
    if not required_cols.issubset(df.columns):
        logging.error(f"âŒ Missing one or more required columns: {required_cols}")
        sys.exit(1)

    # Generate clean edge IDs
    df['edge_id'] = df.apply(
        lambda row: f"{str(row['Line']).strip().replace(' ', '_')}_{row['START_OP']}_{row['END_OP']}", axis=1
    )

    # Rename columns for consistency
    output_df = df[[
        'edge_id',
        'START_OP',
        'START_OP.1',
        'END_OP',
        'END_OP.1',
        'Linie',
        'Line',
        'Geo shape'
    ]].copy()

    output_df.columns = [
        'edge_id',
        'from_stop_id',
        'from_stop_name',
        'to_stop_id',
        'to_stop_name',
        'line_number',
        'line_name',
        'geometry_wkt'
    ]

    try:
        output_df.to_csv(OUTPUT_CSV, sep=";", index=False)
        logging.info(f"âœ… Simplified edges saved to: {OUTPUT_CSV}")
        print(output_df.head(3).to_string(index=False))
    except Exception as e:
        logging.error(f"âŒ Failed to write output file: {e}")
        sys.exit(1)

# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
if __name__ == "__main__":
    main()
