# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# Main analysis logic
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
def analyze_csv(file_path: str):
    logging.info(f"ğŸ“¥ Loading file: {file_path}")
    try:
        df = pd.read_csv(file_path, delimiter=DELIMITER, encoding=ENCODING)
        logging.info(f"âœ… Loaded dataset with shape: {df.shape}\n")
        
        # Print column names
        logging.info("ğŸ§¾ Columns:\n - " + "\n - ".join(df.columns))

        # Print first few rows
        logging.info("\nğŸ” Sample rows:")
        print(df.head(5).to_string(index=False), end="\n\n")

        # General info
        logging.info("ğŸ“Š Dataset Info:")
        print(df.info(), end="\n\n")

        # Null values summary
        logging.info("ğŸ§¼ Null value summary:")
        print(df.isnull().sum(), end="\n\n")

        # Cardinality summary for first few columns
        logging.info("ğŸ“¦ Cardinality preview (top 5 columns):")
        for col in df.columns[:5]:
            print(f" - {col}: {df[col].nunique()} unique")

    except Exception as e:
        logging.error(f"âŒ Failed to analyze CSV: {e}")
        sys.exit(1)

# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
