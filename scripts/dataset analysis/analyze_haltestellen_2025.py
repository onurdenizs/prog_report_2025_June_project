# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
# Logging setup
# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(message)s",
    handlers=[logging.StreamHandler(sys.stdout)]
)

def analyze_csv(file_path: str) -> None:
    try:
        logging.info(f"ğŸ“¥ Loading file: {file_path}")
        df = pd.read_csv(file_path, sep=DELIMITER, quotechar=QUOTECHAR)

        logging.info(f"âœ… Loaded dataset with shape: {df.shape}\n")

        logging.info("ğŸ§¾ Columns:")
        for col in df.columns:
            print(f" - {col}")

        logging.info("\nğŸ” Sample rows:")
        print(df.head(5).to_string(index=False))

        logging.info("\nğŸ“Š Dataset Info:")
        print(df.info())

        logging.info("\nğŸ§¼ Null value summary:")
        print(df.isnull().sum())

        logging.info("\nğŸ“¦ Cardinality preview (top 5 columns):")
        for col in df.columns[:5]:
            print(f" - {col}: {df[col].nunique()} unique")

    except FileNotFoundError:
        logging.error(f"âŒ File not found: {file_path}")
    except Exception as e:
        logging.error(f"âŒ Error while processing file: {e}")

# â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
