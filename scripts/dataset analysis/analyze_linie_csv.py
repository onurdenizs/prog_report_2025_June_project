def analyze_linie_csv(filepath: str, delimiter: str = ";"):
    logging.info(f"ğŸ“¥ Loading file: {filepath}")
    try:
        df = pd.read_csv(filepath, delimiter=delimiter, encoding=ENCODING)
    except Exception as e:
        logging.error(f"âŒ Failed to load CSV: {e}")
        return

    logging.info(f"âœ… Loaded dataset with shape: {df.shape}\n")

    # Display columns
    logging.info("ğŸ§¾ Columns:")
    for col in df.columns:
        print(f" - {col}")
    
    # Display first few rows
    logging.info("\nğŸ” Sample rows:")
    print(df.head(5).to_string(index=False))

    # Summary of data types and non-null counts
    logging.info("\nğŸ“Š Dataset Info:")
    print(df.info())

    # Check for nulls
    logging.info("\nğŸ§¼ Null value summary:")
    print(df.isnull().sum())

    # Unique values in key columns (if known)
    logging.info("\nğŸ“¦ Cardinality preview (top 5 columns):")
    for col in df.columns[:5]:
        unique_vals = df[col].nunique()
        print(f" - {col}: {unique_vals} unique")

